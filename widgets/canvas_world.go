func lowResolutionMapPoints(rows []string) []CanvasPoint {
	points := make([]CanvasPoint, 0, 1024)
	for y, row := range rows {
		for x, r := range []rune(row) {
			if r != '•' {
				continue
			}
			points = append(points, CanvasPoint{
				X: -180 + float64(x)*360/79,
				Y: 90 - float64(y)*180/39,
			})
		}
	}
	return points
}
